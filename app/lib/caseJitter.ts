function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

function jitterInt(value: number, delta: number, min: number, max = Infinity): number {
  const offset = Math.round((Math.random() * 2 - 1) * delta)
  return Math.min(max, Math.max(min, value + offset))
}

function jitterFloat(value: number, delta: number, min: number, decimals: number): number {
  const offset = (Math.random() * 2 - 1) * delta
  const result = Math.max(min, value + offset)
  return parseFloat(result.toFixed(decimals))
}

export function randomPatientName(gender: string): string {
  const g = gender.toLowerCase()
  const first = g.startsWith('f') ? pick(FIRST_NAMES_FEMALE) : pick(FIRST_NAMES_MALE)
  return `${first} ${pick(LAST_NAMES)}`
}

function jitterBP(bp: string): string {
  const m = bp.match(/^(\d+)\s*\/\s*(\d+)/)
  if (!m) return bp
  let sys = parseInt(m[1], 10)
  let dia = parseInt(m[2], 10)
  sys = jitterInt(sys, 4, 70)
  dia = jitterInt(dia, 3, 40)
  if (sys - dia < 20) dia = sys - 20
  return `${sys}/${dia}`
}

function jitterWeight(weight: string): string {
  const m = weight.match(/^(\d+(?:\.\d+)?)\s*(lbs?|lb)?/i)
  if (!m) return weight
  const val = parseFloat(m[1])
  const unit = m[2] ?? 'lbs'
  const jittered = jitterInt(Math.round(val), 5, 50)
  return `${jittered} ${unit}`
}

function escapeRegex(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

function substituteName(text: string, oldName: string, newName: string): string {
  if (!oldName || !newName) return text
  const [oldFirst, ...oldRest] = oldName.trim().split(/\s+/)
  const oldLast = oldRest.length ? oldRest.join(' ') : ''
  const [newFirst, ...newRest] = newName.trim().split(/\s+/)
  const newLast = newRest.length ? newRest.join(' ') : ''

  // Replace longest match first so "John Smith" isn't half-matched before full-match
  let out = text
  out = out.replace(new RegExp(`\\b${escapeRegex(oldName)}\\b`, 'g'), newName)
  if (oldLast && oldLast !== oldName) {
    out = out.replace(new RegExp(`\\b${escapeRegex(oldLast)}\\b`, 'g'), newLast || oldLast)
  }
  // First name only for distinctive tokens (≥4 chars) to avoid false matches on short names
  if (oldFirst && oldFirst.length >= 4 && oldFirst !== oldName) {
    out = out.replace(new RegExp(`\\b${escapeRegex(oldFirst)}\\b`, 'g'), newFirst || oldFirst)
  }
  return out
}

function substituteAge(text: string, oldAge: number, newAge: number): string {
  if (oldAge === newAge) return text
  const o = String(oldAge)
  // "45-year-old" / "45 year old" / "45 years old" etc.
  let out = text.replace(new RegExp(`\\b${o}([\\- ]year[s]?[\\- ]old)\\b`, 'g'), `${newAge}$1`)
  // "45yo" / "45 yo" / "45 y/o" / "45 y.o."
  out = out.replace(new RegExp(`\\b${o}\\s*y\\.?\\s*\\/?\\s*o\\.?\\b`, 'gi'), m => m.replace(o, String(newAge)))
  // "aged 45"
  out = out.replace(new RegExp(`\\baged\\s+${o}\\b`, 'gi'), `aged ${newAge}`)
  // "age: 45" / "age 45"
  out = out.replace(new RegExp(`\\bage[\\s:]+${o}\\b`, 'gi'), m => m.replace(o, String(newAge)))
  return out
}

function rewriteCaseStrings(
  value: unknown,
  oldName: string, newName: string,
  oldAge: number, newAge: number,
): unknown {
  if (typeof value === 'string') return substituteAge(substituteName(value, oldName, newName), oldAge, newAge)
  if (Array.isArray(value)) return value.map(v => rewriteCaseStrings(v, oldName, newName, oldAge, newAge))
  if (value && typeof value === 'object') {
    const out: Record<string, unknown> = {}
    for (const [k, v] of Object.entries(value)) out[k] = rewriteCaseStrings(v, oldName, newName, oldAge, newAge)
    return out
  }
  return value
}

export function jitterCase<T extends {
  patientInfo: { name: string; age: number; gender: string }
  vitals: { bp: string; hr: number; rr: number; temp: number; spo2: number; weight: string }
}>(c: T): T {
  const oldName = c.patientInfo.name
  const oldAge  = c.patientInfo.age
  const newName = randomPatientName(c.patientInfo.gender)
  const ageDelta = oldAge < 5 ? 0 : oldAge < 18 ? 1 : 3
  const newAge   = jitterInt(oldAge, ageDelta, 1)

  // Rewrite all prose strings so name/age in HPI, hiddenHistory, etc. matches the new identity
  const rewritten = rewriteCaseStrings(c, oldName, newName, oldAge, newAge) as T

  return {
    ...rewritten,
    patientInfo: { ...rewritten.patientInfo, name: newName, age: newAge },
    vitals: {
      ...rewritten.vitals,
      bp: jitterBP(c.vitals.bp),
      hr: jitterInt(c.vitals.hr, 3, 30),
      rr: jitterInt(c.vitals.rr, 1, 8),
      temp: jitterFloat(c.vitals.temp, 0.3, 94.0, 1),
      spo2: jitterInt(c.vitals.spo2, 1, 70, 100),
      weight: jitterWeight(c.vitals.weight),
    },
  }
}
