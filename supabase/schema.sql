-- ── Cases library ─────────────────────────────────────────────────────────────
-- Stores the 432 case slots (system × difficulty × diagnosis × variant).
-- case_data is null until a case is first generated by Claude, then cached.

CREATE TABLE IF NOT EXISTS cases (
  id                TEXT        PRIMARY KEY,
  system            TEXT        NOT NULL,
  difficulty        TEXT        NOT NULL,
  diagnosis         TEXT        NOT NULL,
  variant_index     INT         NOT NULL DEFAULT 0,
  case_data         JSONB,
  is_generated      BOOLEAN     NOT NULL DEFAULT FALSE,
  generated_at      TIMESTAMPTZ,
  -- Pre-fetched Open-i image results keyed by imaging test name.
  -- Structure: { "Chest X-Ray": [{uid, imageUrl, thumbnailUrl, caption, modality}], ... }
  -- Empty array = tried + found nothing (needs manual curation).
  -- Missing key = not yet prefetched (trainer will fetch live).
  imaging_cache     JSONB       NOT NULL DEFAULT '{}',
  imaging_cached_at TIMESTAMPTZ,
  created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

